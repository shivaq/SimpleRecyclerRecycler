package yasuaki.kyoto.com.simplerecyclerrecycler.data.model;

public class SubjectCharacter {

  private int mCircle;
  private int mBody;
  private int mEye;
  private int mMouse;

  public int getCircle() {
    return mCircle;
  }

  public void setCircle(int circle) {
    mCircle = circle;
  }

  public int getBody() {
    return mBody;
  }

  public void setBody(int body) {
    mBody = body;
  }

  public int getEye() {
    return mEye;
  }

  public void setEye(int eye) {
    mEye = eye;
  }

  public int getMouse() {
    return mMouse;
  }

  public void setMouse(int mouse) {
    mMouse = mouse;
  }
}
